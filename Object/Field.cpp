Field::Field()
{

	for (int y = 0; y < kBlockNumY; y++)
	{
		for (int x = 0; x < kBlockNumX; x++)
		{
			// モデルクラスのインスタンス作成
			m_pModel[y][x] = std::make_shared<Model>(kFileStageName);
			// 当たり判定用処理
			m_pModel[y][x]->SetUseCollision(true, true);

			m_CoinPosX[y][x] = 0;

		}
	}

	for (int y = 0; y < kBlockNumY; y++)
	{
		for (int x= 0; x < kBlockNumX; x++)
		{
			if (StageData[y][x] == 1)
			{
				// 座標計算
				float posY = 200.0f * ((kBlockNumY - 1) - y) - 300;
				float posX = 200.0f * x ;

				// モデルに位置を渡す
				m_pModel[y][x]->SetPos(VGet(
					posX,
					posY,
					0.0f));
			}

			if (StageData[y][x] == 2)
			{
				coinNum++;
				int posX = 200.0f * x;
				int posY = 200.0f * ((kBlockNumY - 1) - y) - 400;

				m_coinX.push_back(posX);
				m_coinY.push_back(posY);
			}
			if (StageData[y][x] == 3)
			{
				diomondNum++;
				int posX = 200.0f * x;
				int posY = 200.0f * ((kBlockNumY - 1) - y) - 500;

				m_diomondX.push_back(posX);
				m_diomondY.push_back(posY);
			}
		}
	}
}

